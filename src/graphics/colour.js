Colour.create = function (red = 0, green = 0, blue = 0, alpha = 1) {
    return "rgb(" + red + ", " + green + ", " + blue + ", " + alpha + ")";
};

/**
 * Returns a color in the format: "#RRGGBB", or as a hex number if specified.
 *
 * @param {Number|String} colour
 * @param {Boolean=} toNumber=false  Return colour as a hex number.
 * @return String|Number
 */
Colour.parseColour = function (colour, toNumber) {
    if (toNumber === true) {
        if (typeof colour === "number") {
            return (colour | 0); //chop off decimal
        }
        if (typeof colour === "string" && colour[0] === "#") {
            colour = colour.slice(1);
        }
        return window.parseInt(colour, 16);
    } else {
        if (typeof colour === "number") {
            colour = "#" + ("00000" + (colour | 0).toString(16)).substr(-6); //pad
        }
        return colour;
    }
};

Colour.toRgb = function (colourArray) {
    return "rgb(" + colourArray[0] + ", " + colourArray[1] + ", " + colourArray[2] + ", " + colourArray[3] + ")";
};

Colour.of = function (red, green, blue, alpha) {
    return Uint8ClampedArray.of(red, green, blue, alpha);
};

Colour.copy = function (inColour, outColour) {
    outColour[0] = inColour[0];
    outColour[1] = inColour[1];
    outColour[2] = inColour[2];
    outColour[3] = inColour[3];
    return outColour;
};
